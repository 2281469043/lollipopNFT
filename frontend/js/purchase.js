const ADDRESS = "0x213447bfee788d2518390b9A9A87a3cdD276FB7a";

// Create a new contract instance
const contract = new web3.eth.Contract(ABI, ADDRESS);

async function buy_nft(tokenId) {
    console.log('buy_nft() called');

    try {
        // Check if MetaMask is available
        if (!window.ethereum) {
            alert("MetaMask is not available. Please install MetaMask to continue.");
            return;
        }

        // Request account access if needed
        const accounts = await window.ethereum.request({ method: 'eth_requestAccounts' });
        const userAddress = accounts[0];

        // Retrieve NFT details (e.g., price)
        const nftDetails = await contract.methods.getNFTDetails(tokenId).call();
        const price = nftDetails.price;

        // Confirm the purchase
        const confirmation = confirm(`You are about to purchase NFT with Token ID: ${tokenId} for ${web3.utils.fromWei(price, 'ether')} ETH. Do you want to proceed?`);
        if (!confirmation) return;

        // Execute the purchase transaction
        const tx = await contract.methods.purchaseNFT(tokenId).send({
            from: userAddress,
            value: price, // Ensure the price is sent in the transaction
        });

        // Transaction success
        alert(`NFT purchased successfully! Transaction Hash: ${tx.transactionHash}`);
        console.log("Transaction successful:", tx);
    } catch (error) {
        console.error("Error purchasing NFT:", error);
        alert(`Failed to purchase NFT. Error: ${error.message}`);
    }
}
